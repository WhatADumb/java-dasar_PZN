public class Comparison {
    public static void main(String[] args) {
        short a = 100;
        byte b = 100;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
        
    }
}
