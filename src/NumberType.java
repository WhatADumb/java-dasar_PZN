public class NumberType {
    public static void main(String[] args) {
        //Integer Groups
        byte iniByte = 127;
        short iniShort = 10000;
        int iniInt = 10000000;
        long iniLong = 1000000L;

        //Floting Point Groups
        float iniFloat = 3.14F;
        double iniDouble = 3.14;

        //Literals
        int decimal = 145;
        int hexaDecimal = 0x14AF;
        int binary = 0b11101;

        //Underscore
        int dolars = 2;
        int rupiah = 30_000;
    }
}
